namespace DnD5e.Creatures.Spells
{
    /// <summary>
    /// A school of magic.
    /// </summary>
    public enum School
    {
        /// <summary>
        /// Abjuration spells are protective in nature.
        /// </summary>
        Abjuration,

        /// <summary>
        /// Conjuration spells involve the transportation of creatures or objects.
        /// </summary>
        Conjuration,

        /// <summary>
        /// Divination spells reveal information.
        /// </summary>
        Divination,

        /// <summary>
        /// Enchantment spells affect the minds of others.
        /// </summary>
        Enchantment,

        /// <summary>
        /// Evocation spells manipulate magical energy to produce a desired effect.
        /// </summary>
        Evocation,

        /// <summary>
        /// Illusion spells deceive the sense or minds of others.
        /// </summary>
        Illusion,

        /// <summary>
        /// Necromancy spells manipulate the energies of life and death.
        /// </summary>
        Necromancy,

        /// <summary>
        /// Transmuation spells change the properties of a creature, object, or environment.
        /// </summary>
        Transmutation,
    }
}